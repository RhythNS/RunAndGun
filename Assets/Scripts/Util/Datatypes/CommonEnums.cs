/// <summary>
/// Directions on the x-y 2D plane.
/// </summary>
public enum Direction : int
{
    /// <summary>
    /// The positive y-axis direction.
    /// </summary>
    Up = 0,
    /// <summary>
    /// The positive x-axis direction.
    /// </summary>
    Right = 1,
    /// <summary>
    /// The negative y-axis direction.
    /// </summary>
    Down = 2,
    /// <summary>
    /// The negative x-axis direction.
    /// </summary>
    Left = 3
}
